			<div class="maincontent">
				<p><span class="firstword">Welcome</span> to the homepage of the Hydroclimate Extremes Research Group, part of the Civil and Environmental Engineering Department at the University of Wisconsin. We work in a number of research areas, including:</p>
				<ul>
					<li>Measurement, modeling, and analysis of extreme rainfall at high resolution using a variety of sources including rain gages, ground-based weather radar, satellite-based sensors, and numerical weather and climate models.</li>
					<br>
					<li>Understanding the role of rainfall and soil moisture variability and structure and their interactions with watershed properties to produce floods at a wide range of scales using high-resolution multi-scale supercomputer-based watershed models.</li>
					<br>
					<li>Interfacing with meteorologists and climate scientists to translate projected changes in extreme precipitation from regional and global climate models into projections of future risks.</li>
					<br>
					<li>Developing practical tools for improved probabilistic flood hazard and risk estimation, including in urban areas, ungaged watersheds, and developing countries.</li>
				</ul>
			</div>

			<div class="maincontent">
				<p><span class="firstword">In addition,</span> we plan to expand our expertise into related areas, including:</p>
				<ul>
					<li>Understanding the role of rainfall variability and structure as a driver of watershed functions including sediment movement and water quality using high-resolution observations, multi-scale models, and probabilistic methods.</li>
					<br>
					<li>Estimating the economic and social impacts of current and future flood threats, as well as the benefits of financial and infrastructure-based risk management strategies.</li>
					<br>
					<li>Interdisciplinary assessment of multiple hazards posed by tropical cyclones in the present and future climate.</li>
					<br>
					<li>Monitoring and projecting changes in rivers, lakes, and wetlands through the fusion of satellite data with ground-based observations and models.</li>
				</ul>
				<p>We invite discussion and collaboration on these topics and any others.  Those interested in undergraduate, graduate, and post-graduate research opportunities should feel free to contact us about possible openings.  Our studies, datasets, and codes can be made available upon request.</p>
			</div>
